package string;

public class StringDemo1 {
	
	public static void main(String[] args) {
		
		String s = "Welcome";
		String s1 = " to Java";
		System.out.println(s.length());
		System.out.println(s.concat(s1)); // combining using a method
		System.out.println(s + s1); // combining using hard coding
	}

}
